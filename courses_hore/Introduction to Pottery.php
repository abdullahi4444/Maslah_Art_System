                    <h2 class="section-title">Course Description</h2>
                    <div class="content-box">
                        <p>This comprehensive introduction to pottery will guide you through the complete ceramic process from raw clay to finished glazed pieces. Perfect for absolute beginners, you'll learn essential hand-building techniques including pinch, coil, and slab construction before progressing to wheel throwing fundamentals. The course covers clay properties, tool usage, surface decoration, glazing, and basic firing knowledge. Through step-by-step demonstrations and hands-on projects, you'll create functional pottery pieces while developing your skills and artistic expression in clay. By course completion, you'll have the foundational knowledge to continue your ceramic journey with confidence, whether for personal enjoyment or as the first step toward professional pottery.</p>
